    <h2 class="h5 mt-4">1. İşlenen Kişisel Veri Kategorileri</h2>
    <p>Yemekhaneci.com.tr platformunu kullanırken aşağıdaki kişisel verileriniz işlenebilir:</p>
    <ul>
      <li><strong>Kimlik:</strong> Ad, soyad (firma temsilcisi olarak)</li>
      <li><strong>İletişim:</strong> E-posta, telefon, adres</li>
      <li><strong>Müşteri İşlem:</strong> Talep detayları, kişi sayısı, tarih, lokasyon</li>
      <li><strong>İşlem Güvenliği:</strong> IP adresi, tarayıcı (User-Agent), giriş kayıtları</li>
      <li><strong>Pazarlama (opt-in):</strong> KVKK onayı verildiyse e-posta gönderim listesi</li>
      <li><strong>Finansal (kurumsal):</strong> Vergi numarası, fatura bilgileri (sipariş aşamasında)</li>
    </ul>

    <h2 class="h5 mt-4">2. İşleme Amaçları</h2>
    <ul>
      <li>Yemekçi-müşteri eşleştirme hizmeti sunmak (sözleşmenin ifası)</li>
      <li>Hızlı teklif taleplerini onaylı yemekçilere yönlendirmek</li>
      <li>Enflasyon hesaplayıcı sonuçlarını talebe göre e-posta ile iletmek (ayrıca onay)</li>
      <li>Platform güvenliğini sağlamak (kötüye kullanım, sahtecilik tespiti)</li>
      <li>Yasal yükümlülüklerin yerine getirilmesi (vergi, faturalandırma)</li>
      <li>Hizmet kalitesini iyileştirmek için anonim analiz</li>
    </ul>

    <h2 class="h5 mt-4">3. Hukuki Sebepler (KVKK m.5)</h2>
    <ul>
      <li><strong>Sözleşmenin kurulması ve ifası</strong> (m.5/2-c): Talep, teklif, sipariş süreçleri</li>
      <li><strong>Hukuki yükümlülük</strong> (m.5/2-ç): Fatura, vergi, KVKK kayıtları</li>
      <li><strong>Meşru menfaat</strong> (m.5/2-f): Platform güvenliği, dolandırıcılık önleme</li>
      <li><strong>Açık rıza</strong> (m.5/1): Pazarlama iletişimi, çerez (zorunlu olmayan)</li>
    </ul>

    <h2 class="h5 mt-4">4. Aktarım</h2>
    <p>Verileriniz aşağıdaki taraflarla — yalnızca işleme amacının gerektirdiği ölçüde — paylaşılır:</p>
    <ul>
      <li>Eşleştirilen yemekçi firma (talep iletişim bilgileriniz)</li>
      <li>Ödeme sağlayıcı (iyzico — sipariş aşamasında, PCI-DSS uyumlu)</li>
      <li>E-posta sağlayıcı (Brevo / Mailtrap)</li>
      <li>SMS sağlayıcı (Netgsm / İletimerkezi)</li>
      <li>Hosting (Hostinger VPS — Türkiye lokasyonlu)</li>
      <li>Yasal mercii talebi halinde resmi kurumlar</li>
    </ul>
    <p>Yurtdışına aktarım <strong>yapılmamaktadır</strong>. CDN (Cloudflare) edge cache yalnızca anonim public içerik için kullanılır.</p>

    <h2 class="h5 mt-4">5. Saklama Süreleri</h2>
    <ul>
      <li>Hesap verisi: Hesabınız aktif olduğu sürece + silme talebinden 30 gün sonra anonimleştirme</li>
      <li>Sipariş ve fatura: 10 yıl (VUK + TTK)</li>
      <li>Audit log: 7 yıl (KVKK denetim izi)</li>
      <li>Hızlı teklif talepleri: 12 ay (sonra anonim metrik)</li>
      <li>Pazarlama izni: Her zaman geri alabilirsiniz</li>
    </ul>

